function parseDate(stringDate) {


    var day = parseInt(stringDate.substring(0, 3));
    var year = parseInt("20" + stringDate.substring(7, 9));

    var month = stringDate.substring(3, 6);

    switch (month) {
    case "Jan":
        month = 1;
        break;
    case "Feb":
        month = 2;
        break;
    case "Mar":
        month = 3;
        break;
    case "Apr":
        month = 4;
        break;
    case "May":
        month = 5;
        break;
    case "Jun":
        month = 6;
        break;
    case "Jul":
        month = 7;
        break;
    case "Aug":
        month = 8;
        break;
    case "Sep":
        month = 9;
        break;
    case "Oct":
        month = 10;
        break;
    case "Nov":
        month = 11;
        break;
    case "Dec":
        month = 12;
        break;
    }
    month = month - 1;

    return new Date(year, month, day);


}


